import React, { createElement } from 'react';
import { tags } from '@/lib/data';

const createFromTag = (tag: string) => {
    const Comp: React.FC<React.ComponentProps<React.HTMLElementType>> = ({ children, ...props }) => {
        return createElement(tag, props, children);
    };
    Comp.displayName = tag.charAt(0).toUpperCase() + tag.slice(1);
    return Comp;
};

const Dynamic = tags.reduce((acc, val) => {
    acc[val.charAt(0).toUpperCase() + val.slice(1)] = createFromTag(val);
    return acc;
}, {} as Record<string, React.FC<React.ComponentProps<React.HTMLElementType>>>);

